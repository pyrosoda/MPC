static void LCD_SendCmd(uint8_t cmd)
{
  uint8_t buf[2] = {0x00, cmd};
  (void)HAL_I2C_Master_Transmit(&hi2c1, LCD_I2C_ADDR, buf, sizeof(buf), 50);
}

static void LCD_SendData(uint8_t data)
{
  uint8_t buf[2] = {0x40, data};
  (void)HAL_I2C_Master_Transmit(&hi2c1, LCD_I2C_ADDR, buf, sizeof(buf), 50);
}

static void LCD_SetDDRAM(uint8_t addr)
{
  // Set DDRAM address command = 0x80 | addr
  LCD_SendCmd((uint8_t)(0x80 | (addr & 0x7F)));
}

static void LCD_PutCharWrap(char c)
{
  // 16x2 LCD DDRAM mapping: line1 = 0x00..0x0F, line2 = 0x40..0x4F
  uint8_t ddram;

  if (cursor < 16) {
    ddram = (uint8_t)(0x00 + cursor);         // 1st line
  } else {
    ddram = (uint8_t)(0x40 + (cursor - 16));  // 2nd line
  }

  LCD_SetDDRAM(ddram);
  LCD_SendData((uint8_t)c);

  cursor = (uint8_t)((cursor + 1) & 0x1F); // wrap 0..31
}

static void LCD_Init_User(void)
{

  HAL_Delay(50);

  LCD_SendCmd(0x38); // Function set: 8-bit, 2 line, 5x8 dots (common)
  HAL_Delay(5);
  LCD_SendCmd(0x0C); // Display ON, cursor OFF, blink OFF
  HAL_Delay(5);
  LCD_SendCmd(0x01); // Clear display
  HAL_Delay(5);
  LCD_SendCmd(0x06); // Entry mode set: increment, no shift
  HAL_Delay(5);

  cursor = 0;
}

